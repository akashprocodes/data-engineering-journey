# Basic assignments (dynamic typing)
x = 10              # int --   this is comment line use to # explain the code
y = 3.14            # float
name = "Akash"      # str
is_active = True    # bool
nothing = None      # NoneType

print("Basic variables:")
print("x =", x, type(x))
print("y =", y, type(y))
print("name =", name, type(name))
print("is_active =", is_active, type(is_active))
print("nothing =", nothing, type(nothing))
print()

