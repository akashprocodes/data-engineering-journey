name = "Akash-Sneha"

# Endswith method to check the ending substring value.
if name.endswith("Sneha"): 
    print("Yes, the string ends with 'Sneha'")


#count method to count number of occurrences of a substring in the string.
substring = "h"
count = name.count(substring)
print(f"The substring '{substring}' occurs {count} times in the string.")

